var a = 10
document.write("The value of a is: " + a)
document.write("<br>,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,,<br><br><br>")

++a
document.write("The value of ++a is: " + a + "<br>")
document.write("Now the value of a is: " + a + "<br><br><br>")

a++
document.write("The value of a++ is: " + a + "<br>")
document.write("Now the value of a is: " + a + "<br><br><br>")

--a
document.write("The value of --a is: " + a + "<br>")
document.write("Now the value of a is: " + a + "<br><br><br>")

a--
document.write("The value of a-- is: " + a + "<br>")
document.write("Now the value of a is: " + a + "<br><br><br>")